package be.arcadeboard.api.game.menu.items;

public abstract class MenuItem {
    private String name = "";
    private int x, y = 0;

    public MenuItem(String name) {
        setName(name);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
